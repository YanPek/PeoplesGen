    Name = n.readlines()
    LastName = ln.readlines()
    MiddleName = mn.readlines()
    Nlines = -1
    Lnlines = -1
    Mnlines = -1
    for line in Name: Nlines += 1
    for line in LastName: Lnlines += 1
    for line in MiddleName: Mnlines += 1
    n.close()
    ln.close()
    mn.close()
